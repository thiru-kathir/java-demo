package de.dmc;

public class Person
{

	private int id;
	private String name;
	private OtherStuff other;

	/**
	 * @return the id
	 */
	public int getId()
	{
		return id;
	}

	/**
	 * @return the name
	 */
	public String getName()
	{
		return name;
	}

	/**
	 * @return the other
	 */
	public OtherStuff getOther()
	{
		return other;
	}


}
